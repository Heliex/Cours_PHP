<?php
// La boucle "for"

/**
 * Boucle qui s’exécute un nombre de fois déterminé, avec une variable d’itération
 * Utilisez la boucle for lorsque vous connaissez à l'avance le nombre d'itérations que vous souhaitez effectuer.
 * La boucle for est idéale pour effectuer des actions répétitives avec une variable d'itération qui change à chaque tour.
 * Cas d'utilisation : Afficher les 10 premiers nombres entiers.
 **/

for ($i = 1; $i <= 10; $i++) {
    echo "Nombre : $i\n";
}

?>